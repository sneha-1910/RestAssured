package Pojo;

public class Employee {

  	  String empName;
  	  long phno;
  	  int age;
	public Employee(String empName, long phno, int age) {
		super();
		this.empName = empName;
		this.phno = phno;
		this.age = age;
	}
     
	public Employee()
	{
		
	}

	public String getEmpName() {
		return empName;
	}

	public void setEmpName(String empName) {
		this.empName = empName;
	}

	public long getPhno() {
		return phno;
	}

	public void setPhno(long phno) {
		this.phno = phno;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
 	  
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
